def main() -> None:
    entries = json.loads(QA_PATH.read_text(encoding="utf-8"))
    now = time.time()
    for i, e in enumerate(NEW_ENTRIES):
        entries.insert(
            0,
            {
                "keys": e["keys"],
                "title": e["title"],
                "answer": e["answer"],
                "ts": now - i * 0.001,
            },
        )
    for e in entries:
        if e.get("title") == "Полосы на печати (banding)":
            ks = e.setdefault("keys", [])
            for k in BANDING_KEYS_EXTRA:
                if k not in ks:
                    ks.append(k)
            break
    QA_PATH.write_text(json.dumps(entries, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Added {len(NEW_ENTRIES)} manual_qa entries, updated banding keys")


if __name__ == "__main__":
    main()
