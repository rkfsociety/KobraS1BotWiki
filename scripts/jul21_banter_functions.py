def _is_vpn_bot_spam(text: str) -> bool:
    """Реклама VPN-ботов (lotvpnbot и т.п.) — не вопрос к вики."""
    if not text or not text.strip():
        return False
    t = re.sub(r"\s+", " ", text.lower()).strip()
    if re.search(r"\b(?:wiki\.anycubic|вики\s+эникуб|не\s+открывает\s+вики)\b", t):
        return False
    return bool(
        re.search(r"\blotvpn\w*\b", t)
        or (
            re.search(r"\bvpn\b", t)
            and re.search(r"\b(?:bot\b|ищите\s+в\s+телеграм|проверено)\b", t)
        )
    )


